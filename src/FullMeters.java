public class FullMeters {
    static int centimeters = 598;
    static int meter = 100; // 1 meter = 100 centimeters

    public static void main(String[] args) {
        int fullMeters = centimeters / meter;
        System.out.println(fullMeters);
    }
}




// Для вычисления используй переменную centimeters и оператор деления.
// Для объявления и инициализации meters используй одну команду.
